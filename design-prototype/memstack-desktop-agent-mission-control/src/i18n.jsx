const I18nContext = createContext(null);

function getInitialLocale() {
  try {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    if (stored === 'en' || stored === 'zh-CN') return stored;
    return window.navigator.language.toLowerCase().startsWith('zh') ? 'zh-CN' : DEFAULT_LOCALE;
  } catch {
    return DEFAULT_LOCALE;
  }
}

function interpolate(template, values = {}) {
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    template,
  );
}

export function I18nProvider({ children }) {
  const [locale, setLocale] = useState(getInitialLocale);

  useEffect(() => {
    document.documentElement.lang = locale;
    try {
      window.localStorage.setItem(STORAGE_KEY, locale);
    } catch {
      // The prototype still works when storage is unavailable.
    }
  }, [locale]);

  const value = useMemo(() => ({
    locale,
    setLocale,
    t(key, values) {
      const translated = messages[locale]?.[key]
        ?? (locale === 'zh-CN' ? literalZh[key] : undefined)
        ?? messages.en[key]
        ?? key;
      return interpolate(translated, values);
    },
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error('useI18n must be used inside I18nProvider');
  return context;
}
